    public static Drawable createReflectedImage(Bitmap originalBitmap) {
        // the Gap
        final int reflectionGap = 4;

        // width
        int width = originalBitmap.getWidth();
        // height
        int height = originalBitmap.getHeight();

        Matrix matrix = new Matrix();
        matrix.preScale(1, -1);
        Bitmap reflectionBitmap = Bitmap.createBitmap(originalBitmap, 0,
                height / 2, width, height / 2, matrix, false);
        Bitmap withReflectionBitmap = Bitmap.createBitmap(width, (height
                + height / 3 + reflectionGap), Bitmap.Config.ARGB_8888);

        Canvas canvas = new Canvas(withReflectionBitmap);

        Paint paint = new Paint();
        canvas.drawBitmap(reflectionBitmap, 0, height + reflectionGap, paint);

        LinearGradient shader = new LinearGradient(0, height + reflectionGap,
                0, height + reflectionGap + height / 5, 0x40ffffff, 0x00ffffff,
                Shader.TileMode.CLAMP);
        paint.setShader(shader);
        paint.setXfermode(new PorterDuffXfermode(PorterDuff.Mode.DST_IN));

        canvas.drawRect(0, height + reflectionGap, width,
                withReflectionBitmap.getHeight(), paint);

        return new FastBitmapDrawable(withReflectionBitmap);
    }

